//! One-off helper: derive P2PK addresses from deterministic secret key bytes.
//! Run with: cargo test --test keygen -- --nocapture

use ergo_lib::ergotree_interpreter::sigma_protocol::private_input::DlogProverInput;
use ergo_lib::ergotree_ir::chain::address::{Address, NetworkAddress, NetworkPrefix};

/// Three deterministic 32-byte secret keys for test signers.
/// These are test-only keys — never used on mainnet.
const SIGNER_SECRET_BYTES: [[u8; 32]; 3] = [
    [
        0x01, 0x23, 0x45, 0x67, 0x89, 0xAB, 0xCD, 0xEF,
        0x01, 0x23, 0x45, 0x67, 0x89, 0xAB, 0xCD, 0xEF,
        0x01, 0x23, 0x45, 0x67, 0x89, 0xAB, 0xCD, 0xEF,
        0x01, 0x23, 0x45, 0x67, 0x89, 0xAB, 0xCD, 0xEF,
    ],
    [
        0xFE, 0xDC, 0xBA, 0x98, 0x76, 0x54, 0x32, 0x10,
        0xFE, 0xDC, 0xBA, 0x98, 0x76, 0x54, 0x32, 0x10,
        0xFE, 0xDC, 0xBA, 0x98, 0x76, 0x54, 0x32, 0x10,
        0xFE, 0xDC, 0xBA, 0x98, 0x76, 0x54, 0x32, 0x10,
    ],
    [
        0xAA, 0xBB, 0xCC, 0xDD, 0x11, 0x22, 0x33, 0x44,
        0xAA, 0xBB, 0xCC, 0xDD, 0x11, 0x22, 0x33, 0x44,
        0xAA, 0xBB, 0xCC, 0xDD, 0x11, 0x22, 0x33, 0x44,
        0xAA, 0xBB, 0xCC, 0xDD, 0x11, 0x22, 0x33, 0x44,
    ],
];

#[test]
fn print_signer_addresses() {
    for (i, secret_bytes) in SIGNER_SECRET_BYTES.iter().enumerate() {
        let dlog_input = DlogProverInput::from_bytes(secret_bytes)
            .expect("valid scalar");
        let prove_dlog = dlog_input.public_image();
        let address = Address::P2Pk(prove_dlog);
        let net_addr = NetworkAddress::new(NetworkPrefix::Mainnet, &address);
        let addr_str = net_addr.to_base58();
        println!("Signer {}: {}", i + 1, addr_str);
    }
}
